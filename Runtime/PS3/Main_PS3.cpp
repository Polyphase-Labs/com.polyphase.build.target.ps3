// C++ pure-virtual-call handler. With -fno-exceptions -fno-rtti the PPU link
// never pulls libsupc++'s copy in, leaving __cxa_pure_virtual UNDEFINED, and
// every abstract-class vtable slot carries a relocation against the null
// section. Enough of those and the loader rejects the SELF. Define it here so
// those relocations resolve and the module loads. (Same class of bug the PSP
// port hit — see Main_PSP.cpp.)
// Unbuffered TTY write (lv2 syscall) — safe from static-init / abort context.
static void Ps3TtyRaw(const char* s)
{
    if (s == nullptr) return;
    u32 w = 0;
    sysTtyWrite(0, s, (u32)strlen(s), &w);
}

extern "C" void __cxa_pure_virtual()
{
    Ps3TtyRaw("[FATAL] __cxa_pure_virtual (pure virtual call)\n");
    for (;;) {}
}

// The engine boots with -fno-exceptions, so any libstdc++ call that THROWS
// (std::vector::at, std::stoi, std::string over-size, bad_alloc, a static
// std::regex, ...) turns into std::terminate -> abort. The RPCS3 run showed
// "Abort called." with NONE of our checkpoints before it, which means a GLOBAL
// CONSTRUCTOR is throwing before main() runs. These handlers print WHAT threw
// (mangled type name) and WHERE an abort came from, so we can pinpoint it.
static void Ps3TerminateHandler()
{
    const std::type_info* t = abi::__cxa_current_exception_type();
    char buf[160];
    std::snprintf(buf, sizeof(buf),
                  "[TERMINATE] uncaught exception, type = %s\n",
                  t ? t->name() : "(no active exception)");
    Ps3TtyRaw(buf);
    for (;;) {}
}

extern "C" void abort(void)
{
    char buf[160];
    std::snprintf(buf, sizeof(buf), "[ABORT] abort() called. RA0=%p RA1=%p\n",
                  __builtin_return_address(0), __builtin_return_address(1));
    Ps3TtyRaw(buf);
    for (;;) {}
}

// newlib's assert() macro routes here. Override it so a failing assert prints
// its file/line/expression to TTY instead of a silent abort — this is the most
// likely cause of the pre-main abort (an External lib — Bullet/recast/Lua — or
// engine global ctor tripping an assert).
extern "C" void __assert_func(const char* file, int line, const char* func, const char* expr)
{
    char buf[256];
    std::snprintf(buf, sizeof(buf), "[ASSERT] %s:%d  %s()  ->  %s\n",
                  file ? file : "?", line, func ? func : "?", expr ? expr : "?");
    Ps3TtyRaw(buf);
    for (;;) {}
}

// The build uses -fstack-protector-all (see Makefile_PS3 CODEGEN_SAFE). newlib
// on PSL1GHT provides neither the guard value nor the failure handler, so define
// both here. A genuine canary trip logs the return address (in the tripping
// function's epilogue) — addr2line on our fixed-base .text (0x10240) names it.
extern "C" { void* __stack_chk_guard = (void*)0x00595e5a5a5a5a5aULL; }
extern "C" void __stack_chk_fail(void)
{
    void* ra = __builtin_return_address(0);
    char buf[96];
    std::snprintf(buf, sizeof(buf), "[STACK_CHK_FAIL] smash in caller ra=%p\n", ra);
    Ps3TtyRaw(buf);
    for (;;) {}
}

// Runs at global-construction time, at a HIGH priority (101) so it executes
// before the engine's default-priority static initializers — installs the
// terminate handler and proves static init started.
__attribute__((constructor(101)))
static void Ps3InstallDiagnostics()
{
    std::set_terminate(&Ps3TerminateHandler);
    Ps3TtyRaw("[E] early-ctor: diagnostics installed\n");
}

// Ordinary-priority static-init canary — runs with the engine's own global
// ctors. If "[E]" prints but this "[0]" does not, an engine static initializer
// between them is what aborts.
namespace
{
    struct Ps3BootCanary
    {
        Ps3BootCanary() { Ps3TtyRaw("[0] static-init reached\n"); }
    } sPs3BootCanary;
}

// -------------------------------------------------------------------------
// Boot-checkpoint log — independent of the engine's SYS_Log so it works
// BEFORE Initialize() runs. Writes to /dev_hdd0/tmp/ (always writable on real
// firmware and under RPCS3) plus stdout (captured by RPCS3's TTY log).
// -------------------------------------------------------------------------

// HDD root, not /dev_hdd0/tmp — sysFsOpen(O_CREAT) creates the file but not a
// missing parent dir, and RPCS3's virtual HDD has no /dev_hdd0/tmp by default,
// so writing there silently failed and produced no trace. The root always
// exists and is writable (appears at <RPCS3>/dev_hdd0/polyphase_ps3.log).
static const char* kBootLogPath = "/dev_hdd0/polyphase_ps3.log";

// RPCS3 captures the lv2 sys_tty_write syscall in its TTY window (and log with
// a "TTY:" prefix), regardless of how newlib wires stdout — the reliable
// diagnostic channel on emulator + real hardware.
static void Ps3_Tty(const char* s)
{
    if (s == nullptr) return;
    u32 written = 0;
    sysTtyWrite(0, s, (u32)strlen(s), &written);
}

static void Ps3_BootLogRaw(const char* msg, bool /*truncate*/)
{
    // TTY (+ stdout) only — /dev_hdd0 root is not writable under RPCS3
    // (cellFsOpen -> CELL_EACCES), so the file write just spams errors.
    if (msg == nullptr) return;
    fputs(msg, stdout); fputc('\n', stdout); fflush(stdout);
    Ps3_Tty(msg); Ps3_Tty("\n");
}

static void Ps3_BootLog(const char* msg) { Ps3_BootLogRaw(msg, false); }

