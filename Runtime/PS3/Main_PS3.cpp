// Pull one "Key=Value" line out of a Config.ini blob (value trimmed). Matches
// only at a line start so "DefaultScene" doesn't match "DefaultEditorScene".
static std::string Ps3IniValue(const std::string& text, const char* key)
{
    const std::string needle = std::string(key) + "=";
    size_t pos = 0;
    while (pos < text.size())
    {
        const size_t eol = text.find('\n', pos);
        const std::string line = text.substr(pos, (eol == std::string::npos) ? std::string::npos : eol - pos);
        if (line.compare(0, needle.size(), needle) == 0)
        {
            std::string v = line.substr(needle.size());
            while (!v.empty() && (v.back() == '\r' || v.back() == '\n' || v.back() == ' ' || v.back() == '\t'))
                v.pop_back();
            return v;
        }
        if (eol == std::string::npos) break;
        pos = eol + 1;
    }
    return std::string();
}

void OctPreInitialize(EngineConfig& config)
{
    Ps3_BootLog("OctPreInitialize enter");
    GetEngineState()->mStandalone = true;

    // Force 720p logical size. Both the EngineConfig (checked by most code)
    // and — after ReadEngineConfig reloads Config.ini — the EngineState copy
    // in OctPostInitialize must be set (see comment there).
    config.mWindowWidth  = 1280;
    config.mWindowHeight = 720;

    config.mEmbeddedScriptCount = gNumEmbeddedScripts;
    config.mEmbeddedScripts     = gEmbeddedScripts;

    // Homebrew-style disk asset loading. The cooked assets (Engine/, <Project>/,
    // AssetRegistry.txt, and the .oct files) are staged under the game's USRDIR,
    // which SYS_GetPolyphasePath() points at, and the PS3 SYS_ file layer now
    // resolves relative asset paths against it. Config.ini there names the
    // project + default scene. The engine's own ReadEngineConfig() reads via
    // fopen(), which can't resolve a path on lv2, so read the essentials here
    // through the SYS_ file layer and set them on the config the engine will use
    // to LoadProject() + Discover the project's Assets/.
    {
        char* iniData = nullptr; uint32_t iniSize = 0;
        SYS_AcquireFileData("Config.ini", false, 0, iniData, iniSize);
        Ps3_BootLog(iniData != nullptr ? "Config.ini read OK" : "Config.ini read FAIL");
        if (iniData != nullptr && iniSize > 0)
        {
            const std::string ini(iniData, iniSize);
            SYS_ReleaseFileData(iniData);

            const std::string project = Ps3IniValue(ini, "Project");
            const std::string scene   = Ps3IniValue(ini, "DefaultScene");
            Ps3_BootLog(("parsed Project='" + project + "' Scene='" + scene + "'").c_str());
            if (!project.empty())
            {
                config.mProjectName = project;
                config.mProjectPath = project + "/" + project + ".octp";
            }
            if (!scene.empty())
            {
                config.mDefaultScene = scene;
            }
            // Scan the staged .oct files on disk rather than relying on the
            // registry file (keeps path handling to one mechanism).
            config.mUseAssetRegistry = false;

            // PHASE-2 RENDER SMOKE TEST: the bundled SC_FullScene is a
            // kitchen-sink scene that trips a separate engine bug in the
            // recursive UI-include loader on PS3. To validate the Phase-2 RSX
            // renderer in isolation, skip the default scene and spawn a
            // camera + cube ourselves in OctPostInitialize. Set to 0 to boot
            // the project's real default scene instead.
#define PS3_RENDER_SMOKE_TEST 0
#if PS3_RENDER_SMOKE_TEST
            config.mDefaultScene = "";
#endif
            Ps3_BootLog("config members set");
        }
        else
        {
            LogWarning("[PS3] Config.ini not found under USRDIR — no project/scene will load");
        }
    }
    Ps3_BootLog("OctPreInitialize exit");
}

void OctPostInitialize()
{
    // OctPreInitialize sets the config BEFORE GameMain's ReadEngineConfig()
    // loads Config.ini, which can overwrite our size with whatever the project
    // authored for desktop. Initialize() then copies the (possibly clobbered)
    // config into EngineState. Renderer::GetViewportWidth/Height reads
    // EngineState live every frame, so re-force here — AFTER ReadEngineConfig
    // and the config→state copy — to keep widget sizing correct.
    const uint32_t beforeW = GetEngineState()->mWindowWidth;
    const uint32_t beforeH = GetEngineState()->mWindowHeight;
    GetEngineState()->mWindowWidth  = 1280;
    GetEngineState()->mWindowHeight = 720;
    LogDebug("[PS3] OctPostInitialize: window %ux%u -> 1280x720", beforeW, beforeH);

#if PS3_RENDER_SMOKE_TEST
    // Spawn a camera + a rotated cube directly into world 0 so the Phase-2 RSX
    // mesh path (GFX_CreateStaticMeshResource + GFX_DrawStaticMeshComp) draws
    // something we can see, independent of the (crashing) default scene.
    {
        World* world = GetWorld(0);
        Ps3_BootLog(world ? "[PS3TEST] world 0 ok" : "[PS3TEST] world 0 NULL");
        if (world != nullptr)
        {
            Camera3D* cam = world->SpawnNode<Camera3D>();
            if (cam != nullptr)
            {
                cam->SetPosition(glm::vec3(0.0f, 0.5f, 4.0f));
                cam->SetRotation(glm::vec3(-7.0f, 0.0f, 0.0f));
                cam->SetFieldOfView(60.0f);
                world->SetActiveCamera(cam);
            }

            StaticMesh* cubeMesh = LoadAsset<StaticMesh>("SM_Cube");
            Ps3_BootLog(cubeMesh ? "[PS3TEST] SM_Cube loaded" : "[PS3TEST] SM_Cube NULL");
            if (cubeMesh != nullptr)
            {
                StaticMesh3D* cube = world->SpawnNode<StaticMesh3D>();
                if (cube != nullptr)
                {
                    cube->SetStaticMesh(cubeMesh);
                    cube->SetPosition(glm::vec3(0.0f, 0.0f, 0.0f));
                    cube->SetRotation(glm::vec3(25.0f, 35.0f, 0.0f));
                }
            }
            Ps3_BootLog("[PS3TEST] spawn done");
        }
    }
#endif
}
